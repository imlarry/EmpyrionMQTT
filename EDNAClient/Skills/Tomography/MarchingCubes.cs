        // Extract iso-surface mesh from field at given iso value.
        // Cells with all 8 corners outside `populatedMask` (within radius 1) are skipped
        // entirely -- the field is mostly zero there, no surface possible.
        public static void Extract(
            DensityField src,
            float iso,
            out float[] positions,
            out int[] indices)
        {
            int W = src.W, H = src.H, D = src.D;
            var field = src.Field;
            var populated = src.Populated;

            // Cache: for each grid edge, the resulting vertex index in `verts`.
            // Edges are addressed by lower-corner (x,y,z) plus an axis (0=X,1=Y,2=Z).
            // Edge lookup table -> linear key.
            var edgeVertex = new Dictionary<long, int>(capacity: 1024);
            var verts = new List<float>(capacity: 4096);
            var tris  = new List<int>(capacity: 4096);

            for (int z = 0; z < D - 1; z++)
                for (int y = 0; y < H - 1; y++)
                    for (int x = 0; x < W - 1; x++)
                    {
                        // Sparse early-out: skip if no populated voxel near this cell.
                        if (!CellNearPopulated(populated, W, H, D, x, y, z)) continue;

                        // Gather 8 corner densities.
                        float v0 = field[(x + 0) + (y + 0) * W + (z + 0) * W * H];
                        float v1 = field[(x + 1) + (y + 0) * W + (z + 0) * W * H];
                        float v2 = field[(x + 1) + (y + 0) * W + (z + 1) * W * H];
                        float v3 = field[(x + 0) + (y + 0) * W + (z + 1) * W * H];
                        float v4 = field[(x + 0) + (y + 1) * W + (z + 0) * W * H];
                        float v5 = field[(x + 1) + (y + 1) * W + (z + 0) * W * H];
                        float v6 = field[(x + 1) + (y + 1) * W + (z + 1) * W * H];
                        float v7 = field[(x + 0) + (y + 1) * W + (z + 1) * W * H];

                        int state = 0;
                        if (v0 >= iso) state |= 1;
                        if (v1 >= iso) state |= 2;
                        if (v2 >= iso) state |= 4;
                        if (v3 >= iso) state |= 8;
                        if (v4 >= iso) state |= 16;
                        if (v5 >= iso) state |= 32;
                        if (v6 >= iso) state |= 64;
                        if (v7 >= iso) state |= 128;

                        int edges = EdgeTable[state];
                        if (edges == 0) continue;

                        // For each of 12 edges, if it's crossed produce a vertex (or reuse).
                        // Map local vertex index -> dense vertex index in `verts`.
                        int[] edgeVertIx = new int[12];
                        for (int e = 0; e < 12; e++)
                        {
                            if ((edges & (1 << e)) == 0) { edgeVertIx[e] = -1; continue; }
                            int a = EdgeConnection[e, 0];
                            int b = EdgeConnection[e, 1];
                            int ax = x + VertexOffset[a, 0];
                            int ay = y + VertexOffset[a, 1];
                            int az = z + VertexOffset[a, 2];
                            int bx = x + VertexOffset[b, 0];
                            int by = y + VertexOffset[b, 1];
                            int bz = z + VertexOffset[b, 2];

                            // Canonical edge key: lower-corner + axis.
                            int lx = ax < bx ? ax : bx;
                            int ly = ay < by ? ay : by;
                            int lz = az < bz ? az : bz;
                            int axis = ax != bx ? 0 : (ay != by ? 1 : 2);
                            long key = ((long)lx) | (((long)ly) << 14) | (((long)lz) << 28) | (((long)axis) << 42);

                            if (!edgeVertex.TryGetValue(key, out int vIx))
                            {
                                float va = field[ax + ay * W + az * W * H];
                                float vb = field[bx + by * W + bz * W * H];
                                float denom = vb - va;
                                float t = denom != 0f ? (iso - va) / denom : 0.5f;
                                if (t < 0f) t = 0f; else if (t > 1f) t = 1f;

                                float px = ax + (bx - ax) * t;
                                float py = ay + (by - ay) * t;
                                float pz = az + (bz - az) * t;

                                vIx = verts.Count / 3;
                                verts.Add(px); verts.Add(py); verts.Add(pz);
                                edgeVertex[key] = vIx;
                            }
                            edgeVertIx[e] = vIx;
                        }

                        for (int t = 0; t < 16; t += 3)
                        {
                            int e0 = TriTable[state, t];
                            if (e0 < 0) break;
                            int e1 = TriTable[state, t + 1];
                            int e2 = TriTable[state, t + 2];
                            tris.Add(edgeVertIx[e0]);
                            tris.Add(edgeVertIx[e1]);
                            tris.Add(edgeVertIx[e2]);
                        }
                    }

            positions = verts.ToArray();
            indices   = tris.ToArray();
        }

        // Cell at (x,y,z) is "near populated" if any of its 8 corners is itself populated
        // or has a populated neighbor within 1 voxel (a 3x3x3 box around each corner).
        // This skips cells deep in empty space without iterating an explicit dilation mask.
        private static bool CellNearPopulated(BitArray pop, int W, int H, int D, int x, int y, int z)
        {
            // Check the cube corners and a 1-voxel radius around the whole cell.
            int x0 = x - 1, x1 = x + 2;
            int y0 = y - 1, y1 = y + 2;
            int z0 = z - 1, z1 = z + 2;
            if (x0 < 0) x0 = 0; if (x1 > W - 1) x1 = W - 1;
            if (y0 < 0) y0 = 0; if (y1 > H - 1) y1 = H - 1;
            if (z0 < 0) z0 = 0; if (z1 > D - 1) z1 = D - 1;

            for (int zz = z0; zz <= z1; zz++)
                for (int yy = y0; yy <= y1; yy++)
                    for (int xx = x0; xx <= x1; xx++)
                        if (pop[xx + yy * W + zz * W * H]) return true;
            return false;
        }
    }
